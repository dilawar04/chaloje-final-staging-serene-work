class ChatyWidget extends HTMLElement {
    constructor() {
        super();
        this.openWidget = this.openWidget.bind(this);
        this.attachShadow({
            mode: "open"
        });
        this.shadowRoot.appendChild(template.content.cloneNode(true));
        this.widgetIcon = this.shadowRoot.querySelector(".chaty-widget-i");
        this.widgetContent = this.shadowRoot.querySelector(".chaty-widget-c");
        this.widget = this.shadowRoot.querySelectorAll(".chaty-widget");
    }
    connectedCallback() {
        if (this.hasAttribute("icons")) {
            const dataJson = JSON.parse(this.getAttribute("icons"));
            dataJson.forEach((data) => {
                let iconType = Object.keys(data) + "";
                let iconLink = Object.values(data) + "";
                let iconEl = this.shadowRoot.querySelector(".chaty-widget-c--" + iconType);
                if (iconEl !== null) {
                    iconEl.style.display = "block";
                    iconEl.getElementsByTagName("a")[0].setAttribute("href", iconLink);
                }
            });
        }
        this.widgetIcon.addEventListener("click", this.openWidget);
    }
    openWidget() {
        this.widgetContent.classList.toggle("active");
        this.widget.forEach((el) => el.classList.toggle("active"));
    }
}
